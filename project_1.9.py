# Получаем число от пользователя
number = int(input("Введите целое положительное число: "))

# Инициализируем переменную для хранения максимальной цифры
max_digit = 0

# Проверяем каждую цифру числа
while number > 0:
    # Получаем текущую цифру
    digit = number % 10

    # Сравниваем с текущей максимальной цифрой и обновляем ее, если необходимо
    if digit > max_digit:
        max_digit = digit

    # Делим число на 10, чтобы перейти к следующей цифре
    number //= 10

# Выводим результат
print("Самая большая цифра:", max_digit)
