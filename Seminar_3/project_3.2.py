# Задание 2.
# Требуется найти в массиве A[1..N] самый близкий по величине элемент
# к заданному числу X. Пользователь в первой строке вводит натуральное
# число N – количество элементов в массиве.
# В последующих строках записаны N целых чисел Ai.
# Последняя строка содержит число X Пример:
# 5 1 2 3 4 5 6 -> 5

# Ввод данных
N = int(input("Введите количество элементов в массиве: "))
A = list(map(int, input("Введите элементы массива, разделенные пробелами: ").split()))
X = int(input("Введите число X: "))

# Инициализация переменных
closest = A[0]
min_diff = abs(X - A[0])

# Поиск ближайшего элемента
for num in A:
    diff = abs(X - num)
    if diff < min_diff:
        min_diff = diff
        closest = num

# Вывод результата
print("Самый близкий элемент к числу X:", closest)
